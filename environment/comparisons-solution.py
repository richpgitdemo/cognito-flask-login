# Step 1: Create two variables
a = 10
b = 20
# Step 2: Compare the variables using different operators and print the results
# Check if a is equal to b
print(a == b)  # Output: False, because 10 is not equal to 20
# Check if a is not equal to b
print(a != b)  # Output: True, because 10 is not equal to 20
# Check if a is greater than b
print(a > b)  # Output: False, because 10 is less than 20
# Check if a is less than b
print(a < b)  # Output: True, because 10 is less than 20
# Check if a is greater than or equal to b
print(a >= b)  # Output: False, because 10 is not greater than or equal to 20
# Check if a is less than or equal to b
print(a <= b)  # Output: True, because 10 is less than or equal to 20
# Bonus: Add a third variable and check if c is between a and b
c = 15
print(a < c < b)  # Output: True, because 15 is greater than 10 and less than 20
# Bonus: Compare strings
word1 = "apple"
word2 = "orange"
# Check if word1 is equal to word2
print(word1 == word2)  # Output: False, because "apple" is not the same as "orange"
# Check if word1 comes before word2 alphabetically
print(word1 < word2)  # Output: True, because "apple" comes before "orange" alphabetically
# Check if word1 comes after word2 alphabetically
print(word1 > word2)  # Output: False, because "apple" does not come after "orange"