# Define variables
recipient_name = "Alex"  # Replace with the recipient's name
sender_name = "Jordan"  # Replace with your name
special_message = "Congratulations on your achievement!"  # Replace with a special message
# Use f-strings to print a personalized greeting
print(f"Hi {recipient_name}, this is {sender_name}. {special_message}")
# Add another f-string for a thank-you message
print(f"Thank you, {recipient_name}, for being amazing!")
# Comments for beginners:
# - f-strings allow you to insert variable values directly into strings.
# - Use curly braces { } to include variables or expressions inside the string.
# - Example: If recipient_name = "Alex", {recipient_name} becomes "Alex" when printed.