my_string = "Python is awesome!"  # Here's the string you'll practice slicing with
# Example of slicing: Extract "Python" from the string
awesome_slice = my_string[10:16]
print(awesome_slice)  # Expected output: Python
python_is_slice = my_string[:9]
print(python_is_slice)
is_awesome_slice = my_string[7:]
print(is_awesome_slice)
reversed_string_slice = my_string[::-1]
print(reversed_string_slice)